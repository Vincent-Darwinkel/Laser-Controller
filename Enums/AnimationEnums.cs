namespace Enums
{
    /// <summary>
    /// Used to set animation speed of a pattern, value can be used for a stopwatch
    /// </summary>
    public enum AnimationSpeed
    {
        NotSet = -1,
        Off = 0,
        Slow = 3,
        Medium = 10,
        Fast = 15,
        VeryFast = 20
    }
}